//=============================================================================
// 描画処理
//=============================================================================
void DrawGoal(void)
{
	XMMATRIX mtxScl, mtxRot, mtxTranslate, mtxWorld;

	// カリング無効
	SetCullingMode(CULL_MODE_NONE);

	for (int i = 0; i < MAX_GOAL; i++)
	{
		if (g_Goal[i].use == FALSE) continue;
		//SetFuchi(1);

		// ワールドマトリックスの初期化
		mtxWorld = XMMatrixIdentity();

		// スケールを反映
		mtxScl = XMMatrixScaling(g_Goal[i].scl.x, g_Goal[i].scl.y, g_Goal[i].scl.z);
		mtxWorld = XMMatrixMultiply(mtxWorld, mtxScl);

		// 回転を反映
		mtxRot = XMMatrixRotationRollPitchYaw(g_Goal[i].rot.x, g_Goal[i].rot.y + XM_PI, g_Goal[i].rot.z);
		mtxWorld = XMMatrixMultiply(mtxWorld, mtxRot);

		// 移動を反映
		mtxTranslate = XMMatrixTranslation(g_Goal[i].pos.x, g_Goal[i].pos.y, g_Goal[i].pos.z);
		mtxWorld = XMMatrixMultiply(mtxWorld, mtxTranslate);

		// ワールドマトリックスの設定
		SetWorldMatrix(&mtxWorld);

		XMStoreFloat4x4(&g_Goal[i].mtxWorld, mtxWorld);


		// モデル描画
		DrawModel(&g_Goal[i].model);
	}

	//SetFuchi(0);

	// カリング設定を戻す
	SetCullingMode(CULL_MODE_BACK);
}

