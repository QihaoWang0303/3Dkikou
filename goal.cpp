{
	return &g_Goal[0];
}

//=============================================================================
// Goalの設定
//=============================================================================
int SetGoal(XMFLOAT3 pos, XMFLOAT3 rot)
{
	int nIdxGoal = -1;

	for (int nCntGoal = 0; nCntGoal < MAX_GOAL; nCntGoal++)
	{
		if (!g_Goal[nCntGoal].use)
		{
			g_Goal[nCntGoal].pos = pos;
			g_Goal[nCntGoal].rot = rot;
			g_Goal[nCntGoal].scl = { 2.0f, 1.0f, 2.0f };
			g_Goal[nCntGoal].use = TRUE;

			// 影の設定
			//g_Goal [nCntGoal] .shadowIdx = CreateShadow(g_Goal[nCntGoal].pos, 0.5f, 0.5f);

			nIdxGoal = nCntGoal;


			break;
		}
	}

	return nIdxGoal;
}
