static GOAL			g_Goal[MAX_GOAL];				// エネミー

int g_Goal_load = 0;


//=============================================================================
// 初期化処理
//=============================================================================
HRESULT InitGoal(void)
{
	for (int i = 0; i < MAX_GOAL; i++)
	{
		LoadModel(MODEL_GOAL, &g_Goal[i].model);
		g_Goal[i].load = TRUE;

		g_Goal[i].pos = XMFLOAT3(0.0f, 0.0f, 0.0f);
		g_Goal[i].rot = XMFLOAT3(0.0f, 0.0f, 0.0f);
		g_Goal[i].scl = XMFLOAT3(1.0f, 1.0f, 1.0f);

		g_Goal[i].spd = 0.0f;			// 移動スピードクリア
		g_Goal[i].size = GOAL_SIZE;	// 当たり判定の大きさ

		// モデルのディフューズを保存しておく。色変え対応の為。
		GetModelDiffuse(&g_Goal[i].model, &g_Goal[i].diffuse[0]);

		XMFLOAT3 pos = g_Goal[i].pos;
		pos.y -= (GOAL_OFFSET_Y - 0.1f);
		g_Goal[i].shadowIdx = CreateShadow(pos, GOAL_SHADOW_SIZE, GOAL_SHADOW_SIZE);

		g_Goal[i].time = 0.0f;			// 線形補間用のタイマーをクリア
		g_Goal[i].tblNo = 0;			// 再生する行動データテーブルNoをセット
		g_Goal[i].tblMax = 0;			// 再生する行動データテーブルのレコード数をセット


		g_Goal[i].use = FALSE;		// TRUE:生きてる

	}


	for (int z = 0; z < GOALMAZE_HEIGHT; z++)
	{
		for (int x = 0; x < GOALMAZE_WIDTH; x++)
		{
			if (goalmaze[z][x] == 4)
			{
				SetGoal({ x * GOAL_SPACINGX-600.0f, GOAL_OFFSET_Y, z * GOAL_SPACINGZ-600.0f }, { 0.0f, 0.0f, 0.0f });

			}

		}
	}
	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitGoal(void)
{

	for (int i = 0; i < MAX_GOAL; i++)
	{
		if (g_Goal[i].load)
		{
			UnloadModel(&g_Goal[i].model);
			g_Goal[i].load = FALSE;
		}
	}

}

