//=============================================================================
// 初期化処理
//=============================================================================
HRESULT InitWall(void)
{
	for (int i = 0; i < MAX_WALL; i++)
	{
		LoadModel(MODEL_WALL, &g_Wall[i].model);
		g_Wall[i].load = TRUE;

		g_Wall[i].pos = XMFLOAT3(0.0f, 0.0f, 0.0f);
		g_Wall[i].rot = XMFLOAT3(0.0f, 0.0f, 0.0f);
		g_Wall[i].scl = XMFLOAT3(1.0f, 1.0f, 1.0f);

		g_Wall[i].spd = 0.0f;			// 移動スピードクリア
		g_Wall[i].size = WALL_SIZE;	// 当たり判定の大きさ

		// モデルのディフューズを保存しておく。色変え対応の為。
		GetModelDiffuse(&g_Wall[i].model, &g_Wall[i].diffuse[0]);

		XMFLOAT3 pos = g_Wall[i].pos;
		pos.y -= (WALL_OFFSET_Y - 0.1f);
		g_Wall[i].shadowIdx = CreateShadow(pos, WALL_SHADOW_SIZE, WALL_SHADOW_SIZE);

		g_Wall[i].time = 0.0f;			// 線形補間用のタイマーをクリア
		g_Wall[i].tblNo = 0;			// 再生する行動データテーブルNoをセット
		g_Wall[i].tblMax = 0;			// 再生する行動データテーブルのレコード数をセット


		g_Wall[i].use = FALSE;		// TRUE:生きてる

	}


	for (int z = 0; z < MAZE_HEIGHT; z++) 
	{
		for (int x = 0; x < MAZE_WIDTH; x++)
		{
			if (maze[z][x] == 1)
			{
				SetWall({ x * WALL_SPACINGX-600.0f, WALL_OFFSET_Y, z * WALL_SPACINGZ - 600.0f }, { 0.0f, 0.0f, 0.0f });

			}
			//else if (maze[z][x] == 2)
			//{
			//	SetWall({ x * WALL_SPACINGX, WALL_OFFSET_Y, z * WALL_SPACINGZ }, { 0.0f, XM_PI/2, 0.0f });

			//}

		}
	}
	return S_OK;
}

