//=============================================================================
// wallの設定
//=============================================================================
int SetWall(XMFLOAT3 pos, XMFLOAT3 rot)
{
	int nIdxWall = -1;

	for (int nCntWall = 0; nCntWall < MAX_WALL; nCntWall++)
	{
		if (!g_Wall[nCntWall].use)
		{
			g_Wall[nCntWall].pos = pos;
			g_Wall[nCntWall].rot = rot;
			g_Wall[nCntWall].scl = { 2.0f, 10.0f, 2.0f };
			g_Wall[nCntWall].use = TRUE;

			// 影の設定
			//g_Wall [nCntWall] .shadowIdx = CreateShadow(g_Wall[nCntWall].pos, 0.5f, 0.5f);

			nIdxWall = nCntWall;


			break;
		}
	}

	return nIdxWall;
}
